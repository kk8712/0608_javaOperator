package org.java.operator;

public class Operator1 {

	public static void main(String[] args) {
		System.out.println("산술 연산자");

		int num1 = 8643;
		int num2 = 41;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println((double) (num1) / num2);
		System.out.println(num1 % num2);
	}
}
