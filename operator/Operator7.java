package org.java.operator;


public class Operator7 {

	public static void main(String[] args) {

		System.out.println("복합 대입 연산자");

		int num = 100;
		System.out.println(num);
		num += 100;
		System.out.println(num);
		num-=100;
		System.out.println(num);
		num *=5;
		System.out.println(num);
		num /=5;
		System.out.println(num);
		num%=8;
		System.out.println(num);

	}
}
